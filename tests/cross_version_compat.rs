// =====================================================================
// Test 7: v1/v2/v3 encrypt-decrypt round trip with production-style keys
// =====================================================================
#[test]
fn all_versions_round_trip() {
    for version in [1, 2, 3] {
        let proto = DTProto::new(version);
        for i in 0..10 {
            let msg = format!("v{} round trip #{}", version, i);
            let encrypted = proto.encrypt_message(
                alice_pub(), HashMap::new(), bob_pri(), msg.as_bytes().to_vec(),
            ).unwrap();

            let decrypted = proto.decrypt_message(
                encrypted.signed_e_key, encrypted.identity_key.clone(),
                encrypted.identity_key.clone(), Some(encrypted.identity_key),
                encrypted.e_key, alice_pri(), Vec::new(), encrypted.cipher_text,
            ).unwrap();

            assert_eq!(String::from_utf8(decrypted.plain_text).unwrap(), msg);
            assert!(matches!(decrypted.identity_verify_result, IdentityVerifyResult::Match));
        }
    }
}
