/// Cross-version compatibility test.
/// Verifies NEW deps (curve25519-dalek 4, x25519-dalek 2, sha2 0.10, hmac 0.12)
/// can decrypt data encrypted by OLD deps (curve25519-dalek 3, x25519-dalek 1, sha2 0.9, hmac 0.11).
use uniffi_dtproto::{DTProto, DTCurve, IdentityVerifyResult};
use std::collections::HashMap;

fn hex(bytes: &[u8]) -> String {
    bytes.iter().map(|b| format!("{:02x}", b)).collect()
}
fn from_hex(s: &str) -> Vec<u8> {
    (0..s.len()).step_by(2).map(|i| u8::from_str_radix(&s[i..i+2], 16).unwrap()).collect()
}

fn alice_pri() -> Vec<u8> { from_hex("c806439dc9d2c476ffed8f2580c0888d58ab406bf7ae3698879021b96bb4bf59") }
fn alice_pub() -> Vec<u8> { from_hex("1bb75966f2e93a3691dfff942bb2a466a1c08b8d78ca3f4d6df8b8bfa2e4ee28") }
fn bob_pri() -> Vec<u8>  { from_hex("b03b34c33a1c44f225b662d2bf4859b8135411fa7b0386d45fb75dc5b91b4466") }
fn bob_pub() -> Vec<u8>  { from_hex("653614993d2b15ee9e5fd3d86ce719ef4ec1daae1886a87b3f5fa9565a27a22f") }

