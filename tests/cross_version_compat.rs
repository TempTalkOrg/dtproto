// =====================================================================
// Test 1: DH shared secrets must be identical
// =====================================================================
#[test]
fn dh_shared_secret_v1_matches_old() {
    let curve = DTCurve;
    let alice_pub_arr: [u8; 32] = alice_pub().try_into().unwrap();
    let bob_pri_arr: [u8; 32] = bob_pri().try_into().unwrap();

    let shared = curve.dhe_key_agreement(&alice_pub_arr, &bob_pri_arr, 1);
    assert_eq!(hex(&shared), "325f239328941ced6e673b86ba41017448e99b649a9c3806c1dd7ca4c477e629",
        "V1 (raw DH) shared secret mismatch!");
}

#[test]
fn dh_shared_secret_v3_matches_old() {
    let curve = DTCurve;
    let alice_pub_arr: [u8; 32] = alice_pub().try_into().unwrap();
    let bob_pri_arr: [u8; 32] = bob_pri().try_into().unwrap();

    let shared = curve.dhe_key_agreement(&alice_pub_arr, &bob_pri_arr, 3);
    assert_eq!(hex(&shared), "e90016ebdd3ff3d04404ef01db3662609fd9cf56cdd57b27cd2ad30188540c0e",
        "V3 (HKDF) shared secret mismatch!");
}

// =====================================================================
// Test 2: Public key derivation must be identical
// =====================================================================
#[test]
fn public_key_derivation_matches_old() {
    let curve = DTCurve;
    let bob_key = curve.from_key(&bob_pri().try_into().unwrap());
    assert_eq!(hex(&bob_key.derive_public_key_bytes()), "653614993d2b15ee9e5fd3d86ce719ef4ec1daae1886a87b3f5fa9565a27a22f",
        "Bob public key derivation mismatch!");

    let alice_key = curve.from_key(&alice_pri().try_into().unwrap());
    assert_eq!(hex(&alice_key.derive_public_key_bytes()), "1bb75966f2e93a3691dfff942bb2a466a1c08b8d78ca3f4d6df8b8bfa2e4ee28",
        "Alice public key derivation mismatch!");
}

// =====================================================================
// Test 3: NEW code decrypts OLD private chat message
// =====================================================================
#[test]
fn decrypt_old_private_message() {
    let proto = DTProto::new(1); // old version used v1 (raw DH)

    let decrypted = proto.decrypt_message(
        from_hex(OLD_MSG_SIGNED_E_KEY),
        from_hex(OLD_MSG_IDENTITY_KEY),
        from_hex(OLD_MSG_IDENTITY_KEY),
        Some(from_hex(OLD_MSG_IDENTITY_KEY)),
        from_hex(OLD_MSG_E_KEY),
        alice_pri(),
        Vec::new(),
        from_hex(OLD_MSG_CIPHER_TEXT),
    ).expect("Failed to decrypt old-version message with new deps!");

    assert_eq!(String::from_utf8(decrypted.plain_text).unwrap(), "hello cross version");
    assert!(matches!(decrypted.identity_verify_result, IdentityVerifyResult::Match));
}

// =====================================================================
// Test 4: NEW code decrypts OLD RTM message + verifies signature
// =====================================================================
#[test]
fn decrypt_old_rtm_message() {
    let aes_key = vec![0x42u8; 32]; // same fixed key used during encryption
    let proto = DTProto::new(1);

    let decrypted = proto.decrypt_rtm_message(
        from_hex(OLD_RTM_SIGNATURE),
        Some(bob_pub()),
        aes_key,
        from_hex(OLD_RTM_CIPHER_TEXT),
    ).expect("Failed to decrypt old-version RTM message with new deps!");

    assert_eq!(String::from_utf8(decrypted.plain_text).unwrap(), "hello rtm compat");
    assert!(decrypted.verified_id_result, "Signature verification failed for old RTM message!");
}

// =====================================================================
// Test 5: NEW code decrypts OLD encrypted key
// =====================================================================
#[test]
fn decrypt_old_encrypted_key() {
    let proto = DTProto::new(1);

    let decrypted = proto.decrypt_key(
        from_hex(OLD_KEY_E_KEY),
        alice_pri(),
        from_hex(OLD_KEY_E_M_KEY_ALICE),
    ).expect("Failed to decrypt old-version encrypted key with new deps!");

    assert_eq!(hex(&decrypted.m_key), OLD_KEY_M_KEY,
        "Decrypted key doesn't match original!");
}

